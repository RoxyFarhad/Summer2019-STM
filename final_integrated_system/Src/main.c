//performs the delay. usDelay is in us
void DWT_Delay(uint32_t usDelay)
{
    uint32_t startTick = DWT->CYCCNT,
             delayTicks = usDelay * (SystemCoreClock/1000000);

    while (DWT->CYCCNT - startTick < delayTicks);
}



//encoder reading function. Directly modifies posArray
void getAMTPos(uint8_t *posArray) {
	//var to help track status and commands
	uint8_t readyToRecv = 0;
	uint8_t READPOS = 0x10;		//read command
	uint8_t NOP = 0x00;				//NOP command
	
	//send read command
	GPIOA->BSRR = 0x100000U;
	HAL_SPI_TransmitReceive(&hspi1, &READPOS, &readyToRecv, 1, 0xFFFF);
	GPIOA->BSRR = 0x0010U;
	DWT_Delay(5);
	
	//keep read until get 10 (ready)
	while (readyToRecv != 0x10) {
		GPIOA->BSRR = 0x100000U;
		HAL_SPI_TransmitReceive(&hspi1, &NOP, &readyToRecv, 1, 0xFFFF);
		GPIOA->BSRR = 0x0010U;
		DWT_Delay(30);
	}
	
	
	//now is ready: get two bytes that is the 12 bit position
	DWT_Delay(5);
	GPIOA->BSRR = 0x100000U;
	HAL_SPI_TransmitReceive(&hspi1, &NOP, posArray, 1, 0xFFFF);
	GPIOA->BSRR = 0x0010U;
	
	DWT_Delay(5);
	GPIOA->BSRR = 0x100000U;
	HAL_SPI_TransmitReceive(&hspi1, &NOP, posArray + 1, 1, 0xFFFF);
	GPIOA->BSRR = 0x0010U;
	
	//pause so encoder has time to get ready for next read
	DWT_Delay(120);
}







